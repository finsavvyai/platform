# Pre-flight checks
pre_flight_check() {
    print_header "Pre-flight Deployment Check"
    
    local check_passed=true
    
    # Check if in git repository
    if ! git rev-parse --git-dir > /dev/null 2>&1; then
        print_error "Not in a git repository"
        print_status "Run: git init && git add . && git commit -m 'Initial commit'"
        check_passed=false
    else
        print_success "Git repository (✓)"
    fi
    
    # Check for uncommitted changes
    if ! git diff --quiet && ! git diff --cached --quiet; then
        print_warning "Uncommitted changes found"
        print_status "Consider committing changes before deployment"
    fi
    
    # Check build files exist
    if [ ! -f backend/package.json ] || [ ! -f frontend/package.json ]; then
        print_error "Missing package.json files"
        check_passed=false
    else
        print_success "Package files (✓)"
    fi
    
    # Check environment files
    if [ ! -f backend/.env ]; then
        print_warning "Backend .env file missing"
        print_status "Will be created during setup"
    else
        print_success "Backend .env (✓)"
    fi
    
    if [ "$check_passed" = false ]; then
        print_error "Pre-flight check failed. Please fix issues above."
        exit 1
    fi
    
    print_success "Pre-flight check passed"
    log "Pre-flight check completed"
}

# Generate deployment configs
generate_configs() {
    print_header "Generating Deployment Configurations"
    
    mkdir -p deploy-configs
    
    # Create deployment configs for different providers
    create_render_yaml
    mv render.yaml deploy-configs/
    
    create_vercel_config
    mv vercel.json deploy-configs/
    
    # Docker Compose
    create_docker_config
    
    # Railway
    create_railway_config
    
    print_success "Deployment configurations generated in deploy-configs/"
    
    echo ""
    echo "📁 Generated configs:"
    echo "   - render.yaml (Render.com)"
    echo "   - vercel.json (Vercel)"
    echo "   - docker-compose.prod.yml (Docker)"
    echo "   - railway.toml (Railway)"
    echo ""
    echo "Copy the appropriate config to your project root and deploy!"
}

create_docker_config() {
    cat > deploy-configs/docker-compose.prod.yml << 'EOF'
version: '3.8'
services:
  backend:
    build:
      context: ./backend
      dockerfile: Dockerfile
    ports:
      - "3001:3001"
    environment:
      - NODE_ENV=production
      - DATABASE_URL=${DATABASE_URL}
    depends_on:
      - postgres
    restart: unless-stopped
    
  frontend:
    build:
      context: ./frontend
      dockerfile: Dockerfile
    ports:
      - "3000:80"
    environment:
      - VITE_API_BASE_URL=http://localhost:3001
    restart: unless-stopped
    
  postgres:
    image: postgres:15
    environment:
      POSTGRES_DB: questro
      POSTGRES_USER: postgres
      POSTGRES_PASSWORD: ${DB_PASSWORD}
    ports:
      - "5432:5432"
    volumes:
      - postgres_data:/var/lib/postgresql/data
    restart: unless-stopped

volumes:
  postgres_data:
EOF
    print_success "Created docker-compose.prod.yml"
}

create_railway_config() {
    cat > deploy-configs/railway.toml << 'EOF'
[build]
builder = "NIXPACKS"

[deploy]
startCommand = "npm start"
restartPolicyType = "ON_FAILURE"
restartPolicyMaxRetries = 10

[[services]]
name = "backend"
source = "backend/"

[[services]]
name = "frontend"
source = "frontend/"
EOF
    print_success "Created railway.toml"
}

# Execute deployment
execute_deployment() {
    case $DEPLOYMENT_METHOD in
        "render")
            deploy_render
            ;;
        "vercel")
            deploy_vercel
            ;;
        "railway")
            deploy_railway
            ;;
        "configs")
            generate_configs
            ;;
        *)
            print_error "Deployment method not implemented yet: $DEPLOYMENT_METHOD"
            return 1
            ;;
    esac
}

# Post-deployment steps
post_deployment() {
    if [ "$DEPLOYMENT_METHOD" != "configs" ]; then
        print_header "Post-Deployment Steps"
        echo ""
        echo "✅ Next Steps:"
        echo "1. 🗄️  Set up your database (if not done already)"
        echo "2. ⚙️  Configure environment variables in your platform"
        echo "3. 🔑 Update CORS origins for your frontend URL"
        echo "4. 🧪 Test your deployed application"
        echo "5. 📊 Set up monitoring and logging"
        echo "6. 🔒 Configure SSL certificates (if needed)"
        echo "7. 📈 Set up analytics and error tracking"
        echo ""
        
        echo "🔗 Important URLs to configure:"
        echo "   - FRONTEND_URL: Your frontend domain"
        echo "   - VITE_API_BASE_URL: Your backend API domain"
        echo "   - CORS_ORIGINS: Frontend domain for API access"
        echo ""
    fi
}

# Main execution function
main() {
    log "Starting Questro deployment script"
    
    print_status "Starting deployment process..."
    echo ""
    
    # Run pre-checks
    check_system_requirements
    pre_flight_check
    
    # Setup environment
    setup_environment
    validate_build
    
    # Show database setup if needed
    if [ ! -f backend/.env ] || ! grep -q "SUPABASE_URL=https://" backend/.env; then
        show_database_setup
    fi
    
    # Install and test
    install_dependencies
    test_build
    
    # Select and execute deployment
    select_deployment_method
    execute_deployment
    
    # Post-deployment instructions
    post_deployment
    
    # Completion
    echo ""
    print_success "🎉 Deployment script completed!"
    print_status "Check $LOG_FILE for detailed logs"
    
    if [ "$DEPLOYMENT_METHOD" != "configs" ]; then
        echo ""
        print_status "⭐ Don't forget to:"
        echo "   - Star the repository"
        echo "   - Configure monitoring"
        echo "   - Set up backups"
        echo "   - Update documentation"
    fi
    
    log "Deployment script completed successfully"
}

# Error handling and cleanup
cleanup() {
    if [ -f "package-lock.json.bak" ]; then
        mv package-lock.json.bak package-lock.json
    fi
}

trap cleanup EXIT

# Run main function if script is executed directly
if [[ "${BASH_SOURCE[0]}" == "${0}" ]]; then
    main "$@"
fi