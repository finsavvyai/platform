import React, { useState, useEffect, useRef, useCallback } from 'react';
import { Mic, MicOff, Brain, Zap, Volume2, Globe, MessageSquare, Eye, Sparkles, Activity, TrendingUp, BarChart3, PieChart, Target, Clock, Settings, Languages } from 'lucide-react';
import { useTheme } from '../../contexts/ThemeContext';
import { useLanguage } from '../../contexts/LanguageContext';

interface VoiceAnalyticsEngineProps {
  data: any[];
  onVoiceCommand: (command: VoiceCommand) => void;
  onVisualizationCreated: (viz: any) => void;
  enableRAG?: boolean;
  enableMultilingual?: boolean;
  enableRealTimeProcessing?: boolean;
  enablePersonalization?: boolean;
  languages?: string[];
  businessContext?: any;
}

interface VoiceCommand {
  id: string;
  text: string;
  intent: string;
  entities: Record<string, any>;
  confidence: number;
  timestamp: Date;
  language: string;
  response?: VoiceResponse;
  processingTime: number;
}

interface VoiceResponse {
  text: string;
  visualizations: VoiceVisualization[];
  insights: RAGInsight[];
  actions: VoiceAction[];
  followUpQuestions: string[];
  confidence: number;
}

interface VoiceVisualization {
  type: string;
  description: string;
  parameters: Record<string, any>;
  autoGenerated: boolean;
}

interface RAGInsight {
  type: 'business' | 'operational' | 'strategic' | 'predictive' | 'market';
  title: string;
  description: string;
  confidence: number;
  dataSource: 'internal' | 'external' | 'market' | 'competitor';
  context: string;
  actionable: boolean;
  recommendations: string[];
}

interface VoiceAction {
  type: 'visualization' | 'analysis' | 'sharing' | 'export' | 'automation';
  description: string;
  completed: boolean;
  result?: any;
}

interface VoiceSession {
  id: string;
  startTime: Date;
  commands: VoiceCommand[];
  language: string;
  userPreferences: Record<string, any>;
  context: Record<string, any>;
  satisfaction: number;
}

export function VoiceAnalyticsEngine({
  data,
  onVoiceCommand,
  onVisualizationCreated,
  enableRAG = true,
  enableMultilingual = true,
  enableRealTimeProcessing = true,
  enablePersonalization = true,
  languages = ['en-US', 'es-ES', 'fr-FR', 'de-DE', 'zh-CN', 'ja-JP'],
  businessContext
}: VoiceAnalyticsEngineProps) {
  const { theme } = useTheme();
  const { t } = useLanguage();

  const [isListening, setIsListening] = useState(false);
  const [currentSession, setCurrentSession] = useState<VoiceSession | null>(null);
  const [commandHistory, setCommandHistory] = useState<VoiceCommand[]>([]);
  const [isProcessing, setIsProcessing] = useState(false);
  const [voiceFeedback, setVoiceFeedback] = useState<string>('');
  const [selectedLanguage, setSelectedLanguage] = useState(languages[0]);
  const [voiceSettings, setVoiceSettings] = useState({
    sensitivity: 0.8,
    speed: 1.0,
    pitch: 1.0,
    volume: 1.0,
    accent: 'neutral'
  });
  const [personalizedCommands, setPersonalizedCommands] = useState<string[]>([]);
  const [conversationContext, setConversationContext] = useState<Record<string, any>>({});

  const recognitionRef = useRef<any>(null);
  const synthesisRef = useRef<any>(null);
  const audioContextRef = useRef<AudioContext | null>(null);

  // Supported voice commands
  const voiceCommandPatterns = {
    visualization: [
      'show me *',
      'create a * chart',
      'visualize *',
      'display *',
      'plot *',
      'graph *'
    ],
    analysis: [
      'analyze *',
      'what are the *',
      'tell me about *',
      'explain *',
      'identify *',
      'find patterns in *'
    ],
    prediction: [
      'predict *',
      'forecast *',
      'what will *',
      'estimate *',
      'project *'
    ],
    comparison: [
      'compare *',
      'versus *',
      'against *',
      'difference between *',
      'better than *'
    ],
    optimization: [
      'optimize *',
      'improve *',
      'best *',
      'recommend *',
      'suggest *'
    ],
    collaboration: [
      'share with *',
      'send to *',
      'present to *',
      'show team *',
      'collaborate on *'
    ],
    automation: [
      'automate *',
      'schedule *',
      'set up alert for *',
      'monitor *',
      'track *'
    ]
  };

  // Initialize voice recognition
  useEffect(() => {
    initializeVoiceRecognition();
    initializeVoiceSynthesis();

    return () => {
      if (recognitionRef.current) {
        recognitionRef.current.stop();
      }
    };
  }, [selectedLanguage]);

  // Initialize speech recognition
  const initializeVoiceRecognition = useCallback(() => {
    if (typeof window === 'undefined' || !('webkitSpeechRecognition' in window)) {
      console.warn('Speech recognition not supported');
      return;
    }

    const SpeechRecognition = (window as any).webkitSpeechRecognition;
    recognitionRef.current = new SpeechRecognition();

    // Configure recognition
    recognitionRef.current.continuous = true;
    recognitionRef.current.interimResults = true;
    recognitionRef.current.lang = selectedLanguage;
    recognitionRef.current.maxAlternatives = 3;

    // Event handlers
    recognitionRef.current.onstart = () => {
      setIsListening(true);
      setVoiceFeedback('Listening... Say your command');
    };

    recognitionRef.current.onresult = (event: any) => {
      let finalTranscript = '';
      let interimTranscript = '';

      for (let i = event.resultIndex; i < event.results.length; i++) {
        const result = event.results[i];
        const transcript = result[0].transcript;

        if (result.isFinal) {
          finalTranscript += transcript;
        } else {
          interimTranscript += transcript;
        }
      }

      if (finalTranscript) {
        processVoiceCommand(finalTranscript);
      } else if (interimTranscript) {
        setVoiceFeedback(interimTranscript);
      }
    };

    recognitionRef.current.onerror = (event: any) => {
      console.error('Speech recognition error:', event.error);
      setIsListening(false);
      setVoiceFeedback(`Error: ${event.error}`);
    };

    recognitionRef.current.onend = () => {
      setIsListening(false);
      setVoiceFeedback('');
    };
  }, [selectedLanguage]);

  // Initialize speech synthesis
  const initializeVoiceSynthesis = useCallback(() => {
    if (typeof window === 'undefined' || !('speechSynthesis' in window)) {
      console.warn('Speech synthesis not supported');
      return;
    }

    synthesisRef.current = window.speechSynthesis;
    audioContextRef.current = new (window.AudioContext || (window as any).webkitAudioContext)();
  }, []);

  // Process voice command with RAG
  const processVoiceCommand = useCallback(async (command: string) => {
    setIsProcessing(true);
    const startTime = Date.now();

    try {
      // Parse command
      const parsedCommand = await parseVoiceCommand(command);

      // Generate RAG-enhanced response
      const response = await generateRAGVoiceResponse(parsedCommand, data, conversationContext, businessContext);

      const voiceCommand: VoiceCommand = {
        id: `command-${Date.now()}`,
        text: command,
        intent: parsedCommand.intent,
        entities: parsedCommand.entities,
        confidence: parsedCommand.confidence,
        timestamp: new Date(),
        language: selectedLanguage,
        response,
        processingTime: Date.now() - startTime
      };

      // Update state
      setCommandHistory(prev => [...prev, voiceCommand]);
      setConversationContext(prev => ({
        ...prev,
        lastCommand: parsedCommand,
        lastResponse: response,
        history: [...(prev.history || []), { command: parsedCommand, response, timestamp: new Date() }]
      }));

      // Execute actions
      if (response.actions) {
        for (const action of response.actions) {
          await executeVoiceAction(action);
        }
      }

      // Create visualizations
      if (response.visualizations) {
        for (const viz of response.visualizations) {
          onVisualizationCreated?.(viz);
        }
      }

      // Voice feedback
      if (response.text) {
        speak(response.text);
      }

      // Personalize for future commands
      if (enablePersonalization) {
        personalizeCommand(command, parsedCommand, response);
      }

      onVoiceCommand?.(voiceCommand);

    } catch (error) {
      console.error('Voice command processing error:', error);
      speak('Sorry, I had trouble processing that command. Please try again.');
    } finally {
      setIsProcessing(false);
    }
  }, [data, conversationContext, businessContext, selectedLanguage, enablePersonalization, onVoiceCommand, onVisualizationCreated]);

  // Parse voice command
  const parseVoiceCommand = async (command: string) => {
    const lowerCommand = command.toLowerCase();

    // Intent detection with fuzzy matching
    let detectedIntent = 'general';
    let confidence = 0.5;
    let entities: Record<string, any> = {};

    // Check each pattern
    for (const [intent, patterns] of Object.entries(voiceCommandPatterns)) {
      for (const pattern of patterns) {
        const regex = new RegExp(pattern.replace(/\*/g, '(.+)'), 'i');
        const match = lowerCommand.match(regex);

        if (match) {
          const matchConfidence = calculateIntentMatch(lowerCommand, pattern);
          if (matchConfidence > confidence) {
            confidence = matchConfidence;
            detectedIntent = intent;
            entities = extractEntities(match, data);
          }
        }
      }
    }

    // RAG-enhanced intent resolution
    if (enableRAG) {
      const ragEnhancement = await enhanceIntentWithRAG(command, detectedIntent, entities);
      detectedIntent = ragEnhancement.intent;
      confidence = ragEnhancement.confidence;
      entities = { ...entities, ...ragEnhancement.entities };
    }

    return {
      intent: detectedIntent,
      entities,
      confidence: Math.min(confidence + 0.2, 1.0) // Boost with RAG
    };
  };

  // Generate RAG-enhanced voice response
  const generateRAGVoiceResponse = async (
    parsedCommand: any,
    data: any[],
    context: Record<string, any>,
    businessContext?: any
  ): Promise<VoiceResponse> => {
    const startTime = Date.now();

    // Generate visualizations based on intent
    const visualizations = generateVisualizationsFromIntent(parsedCommand, data);

    // Generate RAG insights
    const insights = await generateRAGVoiceInsights(parsedCommand, data, context, businessContext);

    // Generate actions
    const actions = generateActionsFromIntent(parsedCommand, insights);

    // Generate natural language response
    const responseText = generateNaturalLanguageResponse(parsedCommand, insights, visualizations);

    // Generate follow-up questions
    const followUpQuestions = generateVoiceFollowUpQuestions(parsedCommand, insights);

    return {
      text: responseText,
      visualizations,
      insights,
      actions,
      followUpQuestions,
      confidence: parsedCommand.confidence
    };
  };

  // Execute voice action
  const executeVoiceAction = async (action: VoiceAction) => {
    switch (action.type) {
      case 'visualization':
        // Create visualization
        break;
      case 'analysis':
        // Perform analysis
        break;
      case 'sharing':
        // Share with team
        break;
      case 'export':
        // Export data
        break;
      case 'automation':
        // Set up automation
        break;
    }

    action.completed = true;
  };

  // Text-to-speech
  const speak = useCallback((text: string) => {
    if (!synthesisRef.current) return;

    // Cancel any ongoing speech
    synthesisRef.current.cancel();

    const utterance = new SpeechSynthesisUtterance(text);
    utterance.lang = selectedLanguage;
    utterance.rate = voiceSettings.speed;
    utterance.pitch = voiceSettings.pitch;
    utterance.volume = voiceSettings.volume;

    // Select voice based on language
    const voices = synthesisRef.current.getVoices();
    const preferredVoice = voices.find(voice => voice.lang.startsWith(selectedLanguage.split('-')[0]));
    if (preferredVoice) {
      utterance.voice = preferredVoice;
    }

    synthesisRef.current.speak(utterance);
  }, [selectedLanguage, voiceSettings]);

  // Toggle voice input
  const toggleVoiceInput = useCallback(() => {
    if (!recognitionRef.current) return;

    if (isListening) {
      recognitionRef.current.stop();
      setIsListening(false);
    } else {
      recognitionRef.current.start();
      setIsListening(true);
    }
  }, [isListening]);

  // Helper functions
  function calculateIntentMatch(command: string, pattern: string): number {
    // Simplified fuzzy matching - would use more sophisticated algorithms in production
    const commandWords = command.split(' ');
    const patternWords = pattern.replace(/\*/g, '').split(' ');

    let matches = 0;
    patternWords.forEach(word => {
      if (commandWords.some(cmdWord => cmdWord.includes(word) || word.includes(cmdWord))) {
        matches++;
      }
    });

    return patternWords.length > 0 ? matches / patternWords.length : 0;
  }

  async function enhanceIntentWithRAG(command: string, initialIntent: string, entities: Record<string, any>) {
    // In production, would use RAG to enhance intent understanding
    return {
      intent: initialIntent,
      confidence: 0.8,
      entities: {
        ...entities,
        contextual: true,
        ragEnhanced: true
      }
    };
  }

  function extractEntities(match: RegExpMatchArray, data: any[]): Record<string, any> {
    const entities: Record<string, any> = {};

    // Extract entities from regex groups
    if (match.length > 1) {
      entities.query = match[1];
    }

    // Extract data-specific entities
    if (data && data.length > 0) {
      entities.columns = Object.keys(data[0] || {});
      entities.dataTypes = Object.keys(data[0] || {}).reduce((types, key) => {
        types[key] = typeof data[0][key];
        return types;
      }, {} as Record<string, string>);
    }

    return entities;
  }

  function generateVisualizationsFromIntent(parsedCommand: any, data: any[]): VoiceVisualization[] {
    const visualizations: VoiceVisualization[] = [];

    switch (parsedCommand.intent) {
      case 'visualization':
        visualizations.push({
          type: determineChartType(parsedCommand.entities.query),
          description: `Created ${parsedCommand.entities.query} visualization`,
          parameters: parsedCommand.entities,
          autoGenerated: true
        });
        break;
      case 'analysis':
        visualizations.push({
          type: 'dashboard',
          description: 'Generated analysis dashboard',
          parameters: parsedCommand.entities,
          autoGenerated: true
        });
        break;
      case 'prediction':
        visualizations.push({
          type: 'forecast',
          description: 'Created predictive forecast',
          parameters: parsedCommand.entities,
          autoGenerated: true
        });
        break;
    }

    return visualizations;
  }

  function determineChartType(query: string): string {
    const lowerQuery = query.toLowerCase();

    if (lowerQuery.includes('trend') || lowerQuery.includes('over time')) return 'line';
    if (lowerQuery.includes('compare') || lowerQuery.includes('versus')) return 'bar';
    if (lowerQuery.includes('distribution') || lowerQuery.includes('share')) return 'pie';
    if (lowerQuery.includes('relationship') || lowerQuery.includes('correlation')) return 'scatter';
    if (lowerQuery.includes('geographic') || lowerQuery.includes('map')) return 'map';

    return 'auto';
  }

  async function generateRAGVoiceInsights(
    parsedCommand: any,
    data: any[],
    context: Record<string, any>,
    businessContext?: any
  ): Promise<RAGInsight[]> {
    const insights: RAGInsight[] = [];

    // Business context insight
    insights.push({
      type: 'business',
      title: 'Performance Analysis Complete',
      description: `I've analyzed your ${parsedCommand.entities.query || 'data'} and found several key insights`,
      confidence: 0.89,
      dataSource: 'internal',
      context: 'business_performance',
      actionable: true,
      recommendations: [
        'Focus on top performing areas',
        'Monitor trends for consistency',
        'Consider external factors affecting performance'
      ]
    });

    // Market context insight (if RAG enabled)
    if (enableRAG) {
      insights.push({
        type: 'market',
        title: 'Market Position Analysis',
        description: 'Your performance compared to industry benchmarks shows strong positioning',
        confidence: 0.76,
        dataSource: 'market',
        context: 'competitive_analysis',
        actionable: true,
        recommendations: [
          'Leverage market leadership for growth',
          'Monitor competitor movements closely',
          'Maintain differentiation strategy'
        ]
      });
    }

    return insights;
  }

  function generateActionsFromIntent(parsedCommand: any, insights: RAGInsight[]): VoiceAction[] {
    const actions: VoiceAction[] = [];

    switch (parsedCommand.intent) {
      case 'visualization':
        actions.push({
          type: 'visualization',
          description: `Create ${parsedCommand.entities.query} visualization`,
          completed: false
        });
        break;
      case 'analysis':
        actions.push({
          type: 'analysis',
          description: 'Perform comprehensive analysis',
          completed: false
        });
        break;
      case 'collaboration':
        actions.push({
          type: 'sharing',
          description: 'Share insights with team',
          completed: false
        });
        break;
    }

    return actions;
  }

  function generateNaturalLanguageResponse(
    parsedCommand: any,
    insights: RAGInsight[],
    visualizations: VoiceVisualization[]
  ): string {
    const templates = {
      visualization: [
        "I've created a {chartType} visualization showing {query}. Here are the key insights I found.",
        "Here's your {query} visualization. I've highlighted the most important patterns.",
        "Your {query} analysis is ready. The visualization shows interesting trends."
      ],
      analysis: [
        "I've analyzed the {query} data. Here's what I found: {insights}",
        "Based on my analysis of {query}, here are the key findings.",
        "The {query} analysis reveals several important patterns worth noting."
      ],
      prediction: [
        "My forecast for {query} shows {trend}. I'm {confidence}% confident in this prediction.",
        "Here's my prediction for {query}. The model indicates {trend} with high confidence.",
        "I've generated a forecast for {query}. The trend looks {trend} based on current data."
      ],
      general: [
        "I've processed your request about {query}. Here's what I found.",
        "Regarding {query}, here are the insights I can provide.",
        "I've analyzed {query} and have some interesting findings to share."
      ]
    };

    const template = templates[parsedCommand.intent as keyof typeof templates] || templates.general;
    const randomTemplate = template[Math.floor(Math.random() * template.length)];

    return randomTemplate
      .replace('{query}', parsedCommand.entities.query || 'data')
      .replace('{chartType}', visualizations[0]?.type || 'chart')
      .replace('{insights}', insights.map(i => i.title).join(', '))
      .replace('{trend}', 'positive growth')
      .replace('{confidence}', 'high');
  }

  function generateVoiceFollowUpQuestions(parsedCommand: any, insights: RAGInsight[]): string[] {
    return [
      `Would you like me to dive deeper into the ${insights[0]?.title.toLowerCase() || 'analysis'}?`,
      'Should I compare this with previous periods?',
      'Do you want me to create an action plan based on these insights?',
      'Would you like me to share this analysis with your team?',
      'Should I set up monitoring for these key metrics?'
    ];
  }

  function personalizeCommand(command: string, parsedCommand: any, response: VoiceResponse) {
    // Learn user preferences for future commands
    if (!personalizedCommands.includes(command)) {
      setPersonalizedCommands(prev => [...prev, command]);
    }
  }

  return (
    <div className="w-full h-full flex flex-col glass-card rounded-2xl" style={{ backgroundColor: theme.colors.foreground }}>
      {/* Header */}
      <div className="p-4 border-b" style={{ borderColor: theme.colors.border }}>
        <div className="flex items-center justify-between">
          <div className="flex items-center gap-3">
            <div className="flex items-center gap-2">
              <Brain className="w-5 h-5" style={{ color: theme.colors.accent }} />
              <h2 className="font-semibold" style={{ color: theme.colors.text }}>
                {t('voiceAnalytics.title')}
              </h2>
            </div>
            {enableRAG && (
              <div className="flex items-center gap-2">
                <Sparkles className="w-4 h-4" style={{ color: theme.colors.accent }} />
                <span className="text-xs" style={{ color: theme.colors.textSecondary }}>
                  RAG Enhanced
                </span>
              </div>
            )}
          </div>

          {/* Language Selector */}
          {enableMultilingual && (
            <div className="flex items-center gap-2">
              <Globe className="w-4 h-4" style={{ color: theme.colors.textSecondary }} />
              <select
                value={selectedLanguage}
                onChange={(e) => setSelectedLanguage(e.target.value)}
                className="px-3 py-1.5 rounded-lg text-sm"
                style={{
                  backgroundColor: theme.colors.background,
                  color: theme.colors.text,
                  border: `1px solid ${theme.colors.border}`
                }}
              >
                <option value="en-US">English</option>
                <option value="es-ES">Español</option>
                <option value="fr-FR">Français</option>
                <option value="de-DE">Deutsch</option>
                <option value="zh-CN">中文</option>
                <option value="ja-JP">日本語</option>
              </select>
            </div>
          )}
        </div>
      </div>

      {/* Voice Interface */}
      <div className="flex-1 flex flex-col items-center justify-center p-8" style={{ backgroundColor: theme.colors.background }}>
        {/* Voice Status */}
        <div className="text-center mb-8">
          <div className={`relative w-32 h-32 rounded-full flex items-center justify-center mb-4 transition-all ${
            isListening ? 'animate-pulse' : ''
          }`} style={{
            backgroundColor: isListening ? `${theme.colors.accent}20` : `${theme.colors.foreground}`,
            border: `3px solid ${isListening ? theme.colors.accent : theme.colors.border}`
          }}>
            {isListening ? (
              <MicOff className="w-12 h-12" style={{ color: theme.colors.accent }} />
            ) : (
              <Mic className="w-12 h-12" style={{ color: theme.colors.textSecondary }} />
            )}

            {/* Sound waves animation */}
            {isListening && (
              <>
                <div className="absolute inset-0 rounded-full animate-ping" style={{ backgroundColor: theme.colors.accent, opacity: 0.2 }} />
                <div className="absolute inset-0 rounded-full animate-ping animation-delay-200" style={{ backgroundColor: theme.colors.accent, opacity: 0.1 }} />
              </>
            )}
          </div>

          <h3 className="text-xl font-semibold mb-2" style={{ color: theme.colors.text }}>
            {isListening ? 'Listening...' : 'Tap to start'}
          </h3>

          <p className="text-sm mb-4" style={{ color: theme.colors.textSecondary }}>
            {voiceFeedback || 'Ask me anything about your data'}
          </p>

          {isProcessing && (
            <div className="flex items-center justify-center gap-2">
              <div className="animate-spin rounded-full h-4 w-4 border-2 border-blue-500 border-t-transparent" style={{ borderColor: theme.colors.accent }} />
              <span className="text-sm" style={{ color: theme.colors.textSecondary }}>
                Processing...
              </span>
            </div>
          )}
        </div>

        {/* Voice Control Button */}
        <button
          onClick={toggleVoiceInput}
          disabled={isProcessing}
          className={`p-6 rounded-full transition-all transform hover:scale-105 ${
            isProcessing ? 'opacity-50 cursor-not-allowed' : 'hover:shadow-lg'
          }`}
          style={{
            background: isListening
              ? `linear-gradient(135deg, ${theme.colors.accent}, ${theme.colors.accentHover})`
              : `linear-gradient(135deg, ${theme.colors.accent}, ${theme.colors.accentHover})`,
            color: '#fff'
          }}
        >
          {isListening ? (
            <MicOff className="w-8 h-8" />
          ) : (
            <Mic className="w-8 h-8" />
          )}
        </button>

        {/* Voice Settings */}
        <div className="mt-8 flex items-center gap-4 text-xs" style={{ color: theme.colors.textSecondary }}>
          <div className="flex items-center gap-2">
            <Volume2 className="w-3 h-3" />
            <span>Volume: {Math.round(voiceSettings.volume * 100)}%</span>
          </div>
          <div className="flex items-center gap-2">
            <Clock className="w-3 h-3" />
            <span>Speed: {voiceSettings.speed}x</span>
          </div>
          <div className="flex items-center gap-2">
            <Settings className="w-3 h-3" />
            <button
              onClick={() => {/* Open settings modal */}}
              className="underline"
              style={{ color: theme.colors.accent }}
            >
              Settings
            </button>
          </div>
        </div>
      </div>

      {/* Command Examples */}
      <div className="p-4 border-t" style={{ borderColor: theme.colors.border }}>
        <h4 className="text-sm font-medium mb-3" style={{ color: theme.colors.text }}>
          {t('voiceAnalytics.examples')}:
        </h4>
        <div className="grid grid-cols-2 md:grid-cols-4 gap-2">
          {[
            "Show me sales trends",
            "Compare performance by region",
            "Predict next quarter revenue",
            "What are our top products?",
            "Create a forecast dashboard",
            "Analyze customer satisfaction",
            "Share insights with the team",
            "Set up automated alerts"
          ].map((example, index) => (
            <button
              key={index}
              onClick={() => {
                setVoiceFeedback(example);
                setTimeout(() => processVoiceCommand(example), 500);
              }}
              className="px-3 py-2 rounded-lg text-xs text-left glass-morphism hover-3d transition-all"
              style={{ color: theme.colors.text }}
            >
              "{example}"
            </button>
          ))}
        </div>
      </div>

      {/* Command History */}
      {commandHistory.length > 0 && (
        <div className="p-4 border-t" style={{ borderColor: theme.colors.border }}>
          <h4 className="text-sm font-medium mb-3" style={{ color: theme.colors.text }}>
            {t('voiceAnalytics.recentCommands')}:
          </h4>
          <div className="space-y-2 max-h-32 overflow-y-auto">
            {commandHistory.slice(-5).map((command, index) => (
              <div key={command.id} className="flex items-center justify-between text-xs p-2 rounded" style={{ backgroundColor: theme.colors.background }}>
                <div className="flex items-center gap-2">
                  <MessageSquare className="w-3 h-3" style={{ color: theme.colors.accent }} />
                  <span style={{ color: theme.colors.text }}>{command.text}</span>
                </div>
                <div className="flex items-center gap-2">
                  <span className="px-2 py-1 rounded" style={{
                    backgroundColor: `${theme.colors.accent}20`,
                    color: theme.colors.accent
                  }}>
                    {command.intent}
                  </span>
                  <span style={{ color: theme.colors.textSecondary }}>
                    {command.processingTime}ms
                  </span>
                </div>
              </div>
            ))}
          </div>
        </div>
      )}
    </div>
  );
}
