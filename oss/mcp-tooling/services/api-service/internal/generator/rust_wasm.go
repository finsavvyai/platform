// RustWasmGenerator generates Rust code for WASM/native MCP functions
type RustWasmGenerator struct {
	*BaseGenerator
}

// NewRustWasmGenerator creates a new Rust WASM generator
func NewRustWasmGenerator() *RustWasmGenerator {
	features := []Feature{
		FeatureBasicGeneration,
		FeatureTypeGeneration,
		FeatureAuthGeneration,
		FeatureValidation,
		FeatureErrorHandling,
		FeatureRetryLogic,
		FeatureLogging,
		FeatureWASM,
		FeatureRESTSupport,
		FeatureInlineDocs,
		FeatureExamples,
		FeatureAsyncAwait,
	}

	gen := &RustWasmGenerator{
		BaseGenerator: NewBaseGenerator("rust", "wasm", "1.0.0", features),
	}

	return gen
}

// Generate generates Rust code from the intermediate representation
func (g *RustWasmGenerator) Generate(ctx context.Context, ir *parser.IntermediateRepresentation, opts GenerateOptions) (*GeneratedCode, error) {
	startTime := time.Now()

	// Validate IR
	result, err := g.Validate(ir)
	if err != nil {
		return nil, fmt.Errorf("validation failed: %w", err)
	}

	if !result.Valid {
		return nil, fmt.Errorf("IR validation failed: %v", result.Errors)
	}

	files := []GeneratedFile{}

	// Generate main lib.rs file with request handler
	mainFile, err := g.generateMainFile(ir, opts)
	if err != nil {
		return nil, fmt.Errorf("failed to generate main file: %w", err)
	}
	files = append(files, mainFile)

	// Generate types module
	typesFile, err := g.generateTypesFile(ir, opts)
	if err != nil {
		return nil, fmt.Errorf("failed to generate types file: %w", err)
	}
	files = append(files, typesFile)

	// Generate tools module
	toolsFile, err := g.generateToolsFile(ir, opts)
	if err != nil {
		return nil, fmt.Errorf("failed to generate tools file: %w", err)
	}
	files = append(files, toolsFile)

	// Generate client module
	clientFile, err := g.generateClientFile(ir, opts)
	if err != nil {
		return nil, fmt.Errorf("failed to generate client file: %w", err)
	}
	files = append(files, clientFile)

	// Generate Cargo.toml
	cargoFile, err := g.generateCargoToml(ir, opts)
	if err != nil {
		return nil, fmt.Errorf("failed to generate Cargo.toml: %w", err)
	}
	files = append(files, cargoFile)

	// Generate README
	if opts.IncludeDocs {
		readmeFile, err := g.generateReadme(ir, opts)
		if err != nil {
			return nil, fmt.Errorf("failed to generate README: %w", err)
		}
		files = append(files, readmeFile)
	}

	// Generate tests
	if opts.IncludeTests {
		testsFile, err := g.generateTestFile(ir, opts)
		if err != nil {
			return nil, fmt.Errorf("failed to generate tests: %w", err)
		}
		files = append(files, testsFile)
	}

	// Calculate dependencies
	deps := g.generateDependencies(ir, opts)

	// Calculate statistics
	stats := GenerationStatistics{
		TotalFiles:     len(files),
		TotalLines:     g.CountLines(files),
		TotalEndpoints: len(ir.Endpoints),
		TotalTypes:     len(ir.Types),
		GenerationTime: time.Since(startTime),
	}

	return g.CreateGeneratedCode(files, deps, stats), nil
}

// generateMainFile generates the main lib.rs file
func (g *RustWasmGenerator) generateMainFile(ir *parser.IntermediateRepresentation, opts GenerateOptions) (GeneratedFile, error) {
	var sb strings.Builder

	// File header
	sb.WriteString("//! MCP Server for " + ir.Metadata.Title + "\n")
	sb.WriteString("//!\n")
	if ir.Metadata.Description != "" {
		sb.WriteString("//! " + ir.Metadata.Description + "\n")
		sb.WriteString("//!\n")
	}
	sb.WriteString("//! Auto-generated by MCPOverflow\n\n")

	// Imports
	sb.WriteString("use serde::{Deserialize, Serialize};\n")
	sb.WriteString("use serde_json::{json, Value};\n")
	sb.WriteString("use std::collections::HashMap;\n")
	sb.WriteString("use wasm_bindgen::prelude::*;\n\n")

	// Module declarations
	sb.WriteString("pub mod types;\n")
	sb.WriteString("pub mod tools;\n")
	sb.WriteString("pub mod client;\n\n")

	sb.WriteString("use types::*;\n")
	sb.WriteString("use tools::*;\n")
	sb.WriteString("use client::ApiClient;\n\n")

	// Request/Response types
	sb.WriteString("/// HTTP Request structure\n")
	sb.WriteString("#[derive(Debug, Deserialize)]\n")
	sb.WriteString("pub struct Request {\n")
	sb.WriteString("    pub path: String,\n")
	sb.WriteString("    pub method: String,\n")
	sb.WriteString("    #[serde(default)]\n")
	sb.WriteString("    pub headers: HashMap<String, String>,\n")
	sb.WriteString("    #[serde(default)]\n")
	sb.WriteString("    pub body: Option<String>,\n")
	sb.WriteString("}\n\n")

	sb.WriteString("/// HTTP Response structure\n")
	sb.WriteString("#[derive(Debug, Serialize)]\n")
	sb.WriteString("pub struct Response {\n")
	sb.WriteString("    pub status: u16,\n")
	sb.WriteString("    pub headers: HashMap<String, String>,\n")
	sb.WriteString("    pub body: String,\n")
	sb.WriteString("}\n\n")

	sb.WriteString("impl Response {\n")
	sb.WriteString("    fn json(status: u16, data: Value) -> Self {\n")
	sb.WriteString("        let mut headers = HashMap::new();\n")
	sb.WriteString("        headers.insert(\"Content-Type\".to_string(), \"application/json\".to_string());\n")
	sb.WriteString("        headers.insert(\"Access-Control-Allow-Origin\".to_string(), \"*\".to_string());\n")
	sb.WriteString("        \n")
	sb.WriteString("        Response {\n")
	sb.WriteString("            status,\n")
	sb.WriteString("            headers,\n")
	sb.WriteString("            body: data.to_string(),\n")
	sb.WriteString("        }\n")
	sb.WriteString("    }\n\n")

	sb.WriteString("    fn error(status: u16, message: &str) -> Self {\n")
	sb.WriteString("        Self::json(status, json!({ \"error\": message }))\n")
	sb.WriteString("    }\n")
	sb.WriteString("}\n\n")

	// Main handler function
	sb.WriteString("/// Main request handler\n")
	sb.WriteString("#[wasm_bindgen]\n")
	sb.WriteString("pub fn handle_request(request_json: &str) -> String {\n")
	sb.WriteString("    let request: Request = match serde_json::from_str(request_json) {\n")
	sb.WriteString("        Ok(req) => req,\n")
	sb.WriteString("        Err(e) => {\n")
	sb.WriteString("            let err_resp = Response::error(400, &format!(\"Invalid request: {}\", e));\n")
	sb.WriteString("            return serde_json::to_string(&err_resp).unwrap();\n")
	sb.WriteString("        }\n")
	sb.WriteString("    };\n\n")

	sb.WriteString("    let response = match request.path.as_str() {\n")
	sb.WriteString("        \"/.well-known/mcp.json\" => handle_manifest(),\n")
	sb.WriteString("        \"/mcp/execute\" => handle_execute(&request),\n")
	sb.WriteString("        _ => Response::error(404, \"Not Found\"),\n")
	sb.WriteString("    };\n\n")

	sb.WriteString("    serde_json::to_string(&response).unwrap()\n")
	sb.WriteString("}\n\n")

	// Manifest handler
	sb.WriteString("/// Handle MCP manifest request\n")
	sb.WriteString("fn handle_manifest() -> Response {\n")
	sb.WriteString("    let manifest = json!({\n")
	sb.WriteString(fmt.Sprintf("        \"name\": \"%s\",\n", ir.Metadata.Name))
	sb.WriteString(fmt.Sprintf("        \"version\": \"%s\",\n", ir.Metadata.Version))
	if ir.Metadata.Description != "" {
		sb.WriteString(fmt.Sprintf("        \"description\": \"%s\",\n", escapeString(ir.Metadata.Description)))
	}
	sb.WriteString("        \"tools\": get_tools(),\n")
	sb.WriteString("    });\n\n")
	sb.WriteString("    Response::json(200, manifest)\n")
	sb.WriteString("}\n\n")

	// Execute handler
	sb.WriteString("/// Handle tool execution request\n")
	sb.WriteString("fn handle_execute(request: &Request) -> Response {\n")
	sb.WriteString("    let body = match &request.body {\n")
	sb.WriteString("        Some(b) => b,\n")
	sb.WriteString("        None => return Response::error(400, \"Missing request body\"),\n")
	sb.WriteString("    };\n\n")

	sb.WriteString("    let exec_request: ExecuteRequest = match serde_json::from_str(body) {\n")
	sb.WriteString("        Ok(req) => req,\n")
	sb.WriteString("        Err(e) => return Response::error(400, &format!(\"Invalid execute request: {}\", e)),\n")
	sb.WriteString("    };\n\n")

	// Get environment variables
	if NeedsAuthentication(ir) {
		sb.WriteString("    // Get configuration from environment or request\n")
		sb.WriteString("    let api_base_url = std::env::var(\"API_BASE_URL\")\n")
		sb.WriteString(fmt.Sprintf("        .unwrap_or_else(|_| \"%s\".to_string());\n", GetPrimaryServer(ir)))
		sb.WriteString("    let api_key = std::env::var(\"API_KEY\").ok();\n\n")
		sb.WriteString("    let client = ApiClient::new(api_base_url, api_key);\n\n")
	} else {
		sb.WriteString("    let api_base_url = std::env::var(\"API_BASE_URL\")\n")
		sb.WriteString(fmt.Sprintf("        .unwrap_or_else(|_| \"%s\".to_string());\n", GetPrimaryServer(ir)))
		sb.WriteString("    let client = ApiClient::new(api_base_url, None);\n\n")
	}

	// Tool routing
	sb.WriteString("    match exec_request.tool.as_str() {\n")
	for _, endpoint := range ir.Endpoints {
		toolName := g.getToolName(endpoint)
		methodName := toSnakeCase(sanitizeIdentifier(toolName))

		sb.WriteString(fmt.Sprintf("        \"%s\" => {\n", toolName))
		sb.WriteString(fmt.Sprintf("            match client.%s(&exec_request.arguments) {\n", methodName))
		sb.WriteString("                Ok(result) => Response::json(200, result),\n")
		sb.WriteString("                Err(e) => Response::error(500, &format!(\"Tool execution failed: {}\", e)),\n")
		sb.WriteString("            }\n")
		sb.WriteString("        }\n")
	}
	sb.WriteString("        _ => Response::error(404, &format!(\"Unknown tool: {}\", exec_request.tool)),\n")
	sb.WriteString("    }\n")
	sb.WriteString("}\n\n")

	// ExecuteRequest type
	sb.WriteString("/// Tool execution request\n")
	sb.WriteString("#[derive(Debug, Deserialize)]\n")
	sb.WriteString("struct ExecuteRequest {\n")
	sb.WriteString("    tool: String,\n")
	sb.WriteString("    arguments: Value,\n")
	sb.WriteString("}\n")

	return GeneratedFile{
		Path:     "src/lib.rs",
		Content:  sb.String(),
		Type:     FileTypeSource,
		Language: "rust",
	}, nil
}

// generateTypesFile generates the types module
func (g *RustWasmGenerator) generateTypesFile(ir *parser.IntermediateRepresentation, opts GenerateOptions) (GeneratedFile, error) {
	var sb strings.Builder

	sb.WriteString("//! Type definitions for " + ir.Metadata.Title + "\n")
	sb.WriteString("//!\n")
	sb.WriteString("//! Auto-generated by MCPOverflow\n\n")

	sb.WriteString("use serde::{Deserialize, Serialize};\n")
	sb.WriteString("use serde_json::Value;\n\n")

	// Generate type definitions
	for _, typeDef := range ir.Types {
		typeCode := g.generateTypeDefinition(typeDef)
		sb.WriteString(typeCode)
		sb.WriteString("\n")
	}

	// If no types defined, add a placeholder comment
	if len(ir.Types) == 0 {
		sb.WriteString("// No custom types defined\n")
	}

	return GeneratedFile{
		Path:     "src/types.rs",
		Content:  sb.String(),
		Type:     FileTypeSource,
		Language: "rust",
	}, nil
}

// generateTypeDefinition generates a Rust struct from a type definition
func (g *RustWasmGenerator) generateTypeDefinition(typeDef parser.TypeDefinition) string {
	var sb strings.Builder

	structName := toPascalCase(typeDef.Name)

	// Add documentation
	if typeDef.Description != "" {
		sb.WriteString(fmt.Sprintf("/// %s\n", typeDef.Description))
	}

	// Add derives
	sb.WriteString("#[derive(Debug, Clone, Serialize, Deserialize)]\n")

	// Struct definition
	sb.WriteString(fmt.Sprintf("pub struct %s {\n", structName))

	// Add fields
	for propName, propDef := range typeDef.Properties {
		rustType := typeToRust(propDef.Type)

		// Check if field is optional (not in required list)
		isRequired := contains(typeDef.Required, propName)

		if propDef.Description != "" {
			sb.WriteString(fmt.Sprintf("    /// %s\n", propDef.Description))
		}

		if !isRequired {
			sb.WriteString("    #[serde(skip_serializing_if = \"Option::is_none\")]\n")
			rustType = fmt.Sprintf("Option<%s>", rustType)
		}

		sb.WriteString(fmt.Sprintf("    pub %s: %s,\n", toSnakeCase(propName), rustType))
	}

	sb.WriteString("}\n")

	return sb.String()
}

// generateToolsFile generates the tools module
func (g *RustWasmGenerator) generateToolsFile(ir *parser.IntermediateRepresentation, opts GenerateOptions) (GeneratedFile, error) {
	var sb strings.Builder

	sb.WriteString("//! MCP Tools definitions\n")
	sb.WriteString("//!\n")
	sb.WriteString("//! Auto-generated by MCPOverflow\n\n")

	sb.WriteString("use serde_json::{json, Value};\n\n")

	sb.WriteString("/// Get all available MCP tools\n")
	sb.WriteString("pub fn get_tools() -> Vec<Value> {\n")
	sb.WriteString("    vec![\n")

	for _, endpoint := range ir.Endpoints {
		toolName := g.getToolName(endpoint)
		description := endpoint.Description
		if description == "" {
			description = fmt.Sprintf("%s %s", endpoint.Method, endpoint.Path)
		}

		sb.WriteString("        json!({\n")
		sb.WriteString(fmt.Sprintf("            \"name\": \"%s\",\n", toolName))
		sb.WriteString(fmt.Sprintf("            \"description\": \"%s\",\n", escapeString(description)))
		sb.WriteString("            \"inputSchema\": {\n")
		sb.WriteString("                \"type\": \"object\",\n")
		sb.WriteString("                \"properties\": {\n")

		// Add path parameters
		for _, param := range endpoint.Parameters {
			if param.In == "path" {
				sb.WriteString(fmt.Sprintf("                    \"%s\": {\n", param.Name))
				sb.WriteString(fmt.Sprintf("                        \"type\": \"%s\",\n", param.Schema.Type))
				if param.Description != "" {
					sb.WriteString(fmt.Sprintf("                        \"description\": \"%s\",\n", escapeString(param.Description)))
				}
				sb.WriteString("                    },\n")
			}
		}

		// Add query parameters
		for _, param := range endpoint.Parameters {
			if param.In == "query" {
				sb.WriteString(fmt.Sprintf("                    \"%s\": {\n", param.Name))
				sb.WriteString(fmt.Sprintf("                        \"type\": \"%s\",\n", param.Schema.Type))
				if param.Description != "" {
					sb.WriteString(fmt.Sprintf("                        \"description\": \"%s\",\n", escapeString(param.Description)))
				}
				sb.WriteString("                    },\n")
			}
		}

		// Add request body if present
		if endpoint.RequestBody != nil && endpoint.Method != "GET" && endpoint.Method != "DELETE" {
			sb.WriteString("                    \"body\": {\n")
			sb.WriteString("                        \"type\": \"object\",\n")
			if endpoint.RequestBody.Description != "" {
				sb.WriteString(fmt.Sprintf("                        \"description\": \"%s\",\n", escapeString(endpoint.RequestBody.Description)))
			}
			sb.WriteString("                    },\n")
		}

		sb.WriteString("                },\n")

		// Required parameters
		var required []string
		for _, param := range endpoint.Parameters {
			if param.Required && param.In == "path" {
				required = append(required, param.Name)
			}
		}
		if len(required) > 0 {
			sb.WriteString("                \"required\": [")
			for i, r := range required {
				if i > 0 {
					sb.WriteString(", ")
				}
				sb.WriteString(fmt.Sprintf("\"%s\"", r))
			}
			sb.WriteString("],\n")
		}

		sb.WriteString("            },\n")
		sb.WriteString("        }),\n")
	}

	sb.WriteString("    ]\n")
	sb.WriteString("}\n")

	return GeneratedFile{
		Path:     "src/tools.rs",
		Content:  sb.String(),
		Type:     FileTypeSource,
		Language: "rust",
	}, nil
}

