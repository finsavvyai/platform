// getToolName generates a tool name from an endpoint
func (g *RustWasmGenerator) getToolName(endpoint parser.UnifiedEndpoint) string {
	if endpoint.Name != "" {
		return toSnakeCase(endpoint.Name)
	}

	// Use operationId if available
	if endpoint.ID != "" {
		return toSnakeCase(endpoint.ID)
	}

	// Generate from method and path
	method := strings.ToLower(endpoint.Method)
	path := strings.Trim(endpoint.Path, "/")
	path = strings.ReplaceAll(path, "/", "_")
	path = strings.ReplaceAll(path, "{", "")
	path = strings.ReplaceAll(path, "}", "")

	return fmt.Sprintf("%s_%s", method, path)
}
