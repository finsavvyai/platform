// generateClientMethod generates a client method for an endpoint
func (g *RustWasmGenerator) generateClientMethod(endpoint parser.UnifiedEndpoint, ir *parser.IntermediateRepresentation) string {
	var sb strings.Builder

	toolName := g.getToolName(endpoint)
	methodName := toSnakeCase(sanitizeIdentifier(toolName))

	// Documentation
	if endpoint.Description != "" {
		sb.WriteString(fmt.Sprintf("    /// %s\n", endpoint.Description))
	}
	sb.WriteString(fmt.Sprintf("    /// %s %s\n", endpoint.Method, endpoint.Path))

	// Method signature
	sb.WriteString(fmt.Sprintf("    pub fn %s(&self, args: &Value) -> Result<Value, String> {\n", methodName))

	// Build URL with path parameters
	path := endpoint.Path
	for _, param := range endpoint.Parameters {
		if param.In == "path" {
			sb.WriteString(fmt.Sprintf("        let %s = args[\"%s\"].as_str().unwrap_or_default();\n",
				toSnakeCase(param.Name), param.Name))
			path = strings.ReplaceAll(path, "{"+param.Name+"}", fmt.Sprintf("{}", toSnakeCase(param.Name)))
		}
	}

	// Format URL
	pathFormatted := strings.ReplaceAll(path, "{}", "{}")
	var pathArgs []string
	for _, param := range endpoint.Parameters {
		if param.In == "path" {
			pathArgs = append(pathArgs, toSnakeCase(param.Name))
		}
	}

	if len(pathArgs) > 0 {
		sb.WriteString(fmt.Sprintf("        let url = format!(\"{}{}\", self.base_url, format!(\"%s\", %s));\n\n",
			pathFormatted, strings.Join(pathArgs, ", ")))
	} else {
		sb.WriteString(fmt.Sprintf("        let url = format!(\"{}{}\", self.base_url, \"%s\");\n\n", path))
	}

	// Build query parameters
	sb.WriteString("        let mut params = HashMap::new();\n")
	for _, param := range endpoint.Parameters {
		if param.In == "query" {
			sb.WriteString(fmt.Sprintf("        if let Some(val) = args[\"%s\"].as_str() {\n", param.Name))
			sb.WriteString(fmt.Sprintf("            params.insert(\"%s\".to_string(), val.to_string());\n", param.Name))
			sb.WriteString("        }\n")
		}
	}
	sb.WriteString("\n")

	// Request body
	if endpoint.Method == "POST" || endpoint.Method == "PUT" || endpoint.Method == "PATCH" {
		sb.WriteString("        let body = args.get(\"body\");\n\n")
		sb.WriteString(fmt.Sprintf("        self.request(\"%s\", &url, &params, body)\n", endpoint.Method))
	} else {
		sb.WriteString(fmt.Sprintf("        self.request(\"%s\", &url, &params, None)\n", endpoint.Method))
	}

	sb.WriteString("    }\n")

	return sb.String()
}

