// TypeScriptCloudflareGenerator generates TypeScript code for Cloudflare Workers MCP
type TypeScriptCloudflareGenerator struct {
	*BaseGenerator
}

// NewTypeScriptCloudflareGenerator creates a new TypeScript Cloudflare Worker generator
func NewTypeScriptCloudflareGenerator() *TypeScriptCloudflareGenerator {
	features := []Feature{
		FeatureBasicGeneration,
		FeatureTypeGeneration,
		FeatureAuthGeneration,
		FeatureValidation,
		FeatureErrorHandling,
		FeatureRetryLogic,
		FeatureCaching,
		FeatureLogging,
		FeatureCloudflareWorker,
		FeatureRESTSupport,
		FeatureInlineDocs,
		FeatureExamples,
	}

	gen := &TypeScriptCloudflareGenerator{
		BaseGenerator: NewBaseGenerator("typescript", "cloudflare-worker", "1.0.0", features),
	}

	gen.registerTemplates()
	return gen
}

// Generate generates TypeScript code for Cloudflare Workers
func (g *TypeScriptCloudflareGenerator) Generate(ctx context.Context, ir *parser.IntermediateRepresentation, opts GenerateOptions) (*GeneratedCode, error) {
	startTime := time.Now()

	// Validate IR
	result, err := g.Validate(ir)
	if err != nil {
		return nil, fmt.Errorf("validation failed: %w", err)
	}
	if !result.Valid {
		return nil, fmt.Errorf("IR validation failed: %d errors", len(result.Errors))
	}

	files := []GeneratedFile{}

	// Generate main worker file
	mainFile, err := g.generateMainFile(ir, opts)
	if err != nil {
		return nil, fmt.Errorf("failed to generate main file: %w", err)
	}
	files = append(files, mainFile)

	// Generate types file
	typesFile, err := g.generateTypesFile(ir, opts)
	if err != nil {
		return nil, fmt.Errorf("failed to generate types file: %w", err)
	}
	files = append(files, typesFile)

	// Generate tools file
	toolsFile, err := g.generateToolsFile(ir, opts)
	if err != nil {
		return nil, fmt.Errorf("failed to generate tools file: %w", err)
	}
	files = append(files, toolsFile)

	// Generate package.json
	packageFile, err := g.generatePackageJSON(ir, opts)
	if err != nil {
		return nil, fmt.Errorf("failed to generate package.json: %w", err)
	}
	files = append(files, packageFile)

	// Generate wrangler.toml
	wranglerFile, err := g.generateWranglerConfig(ir, opts)
	if err != nil {
		return nil, fmt.Errorf("failed to generate wrangler.toml: %w", err)
	}
	files = append(files, wranglerFile)

	// Generate README
	if opts.IncludeDocs {
		readmeFile, err := g.generateReadme(ir, opts)
		if err != nil {
			return nil, fmt.Errorf("failed to generate README: %w", err)
		}
		files = append(files, readmeFile)
	}

	// Generate tests
	if opts.IncludeTests {
		testFile, err := g.generateTestFile(ir, opts)
		if err != nil {
			return nil, fmt.Errorf("failed to generate tests: %w", err)
		}
		files = append(files, testFile)
	}

	// Create dependencies
	deps := g.generateDependencies(ir, opts)

	// Calculate statistics
	stats := GenerationStatistics{
		TotalFiles:     len(files),
		TotalLines:     g.CountLines(files),
		TotalEndpoints: len(ir.Endpoints),
		TotalTypes:     len(ir.Types),
		GenerationTime: time.Since(startTime),
	}

	return g.CreateGeneratedCode(files, deps, stats), nil
}

