    def _generate_python_methods(self, interface: InterfaceDefinition) -> str:
        """Generate Python method implementations."""
        methods = []

        for method in interface.methods:
            python_name = self.format_method_name(method.name, Language.PYTHON)

            # Build parameter list
            params = ["self"]
            for param in method.parameters:
                param_type = self.map_type(param.type)
                params.append(f"{param.name}: {param_type}")

            param_str = ", ".join(params)

            # Build method body
            docstring = (
                f'"""{method.docstring or method.name}"""' if method.docstring else ""
            )
            method_code = f"    def {python_name}({param_str}) -> {self.map_type(method.return_type)}:\n"

            if docstring:
                method_code += f"        {docstring}\n"

            # Add implementation placeholder
            method_code += "        # TODO: Implement method logic\n"
            method_code += f'        raise NotImplementedError("{python_name} not yet implemented")\n'

            methods.append(method_code)

        return "\n\n".join(methods)

    def _generate_python_imports(self, interface: InterfaceDefinition) -> str:
        """Generate Python import statements."""
        imports = set()

        for method in interface.methods:
            for param in method.parameters:
                self._collect_python_type_imports(param.type, imports)
            self._collect_python_type_imports(method.return_type, imports)

        if imports:
            return "from __future__ import annotations\n\n" + "\n".join(
                f"import {imp}" for imp in sorted(imports)
            )

        return "from __future__ import annotations"

    def _collect_python_type_imports(self, type_str: str, imports: set[str]) -> None:
        """Collect Python imports needed for a type."""
        base_type = type_str.split("<")[0].strip()

        python_imports = {
            "datetime": "datetime",
            "uuid": "uuid",
            "List": "typing.List",
            "Dict": "typing.Dict",
            "Any": "typing.Any",
            "Optional": "typing.Optional",
        }

        if base_type in python_imports:
            imports.add(python_imports[base_type])

    def _generate_java_client_methods(self, interface: InterfaceDefinition) -> str:
        """Generate Java client method implementations."""
        methods = []

        for method in interface.methods:
            java_name = self.format_method_name(method.name, Language.JAVA)

            # Build parameter list
            params = []
            for param in method.parameters:
                param_type = self.map_type(param.type)
                params.append(f"{param_type} {param.name}")

            param_str = ", ".join(params)
            return_type = self.map_type(method.return_type)

            # Generate public method
            method_code = f"        /**\n"
            if method.docstring:
                method_code += f"         * {method.docstring}\n"
            method_code += f"         */\n"
            method_code += f"        public {return_type} {java_name}({param_str}) {{\n"

            # Call through entry point
            entry_point_call = f"entryPoint.{java_name}("
            entry_point_call += ", ".join(p.name for p in method.parameters)
            entry_point_call += ")"

            if return_type == "void":
                method_code += f"            {entry_point_call};\n"
            else:
                method_code += f"            return {entry_point_call};\n"

            method_code += "        }\n\n"

            methods.append(method_code)

        return "\n".join(methods)

    def _generate_java_imports(self, interface: InterfaceDefinition) -> str:
        """Generate Java import statements."""
        imports = set(
            [
                "import py4j.Gateway;",
            ]
        )

        # Add standard Java imports
        for method in interface.methods:
            for param in method.parameters:
                self._collect_java_type_imports(param.type, imports)
            self._collect_java_type_imports(method.return_type, imports)

        return "\n".join(sorted(imports))

    def _collect_java_type_imports(self, type_str: str, imports: set[str]) -> None:
        """Collect Java imports needed for a type."""
        base_type = type_str.split("<")[0].strip()

        java_imports = {
            "List": "import java.util.List;",
            "Map": "import java.util.Map;",
            "ArrayList": "import java.util.ArrayList;",
            "HashMap": "import java.util.HashMap;",
            "UUID": "import java.util.UUID;",
            "Instant": "import java.time.Instant;",
        }

        if base_type in java_imports:
            imports.add(java_imports[base_type])
