"""Py4J bridge template for Python-Java interoperability."""

from __future__ import annotations

import textwrap
from typing import List, Optional

from .base import (
    BridgeConfig,
    BridgeTemplate,
    BridgeType,
    InterfaceDefinition,
    Language,
)


class Py4JTemplate(BridgeTemplate):
    """Generate Py4J bridge code for Python-Java interoperability."""

    def __init__(self, config: BridgeConfig) -> None:
        # Override config to ensure proper settings for Py4J
        config.bridge_type = BridgeType.PY4J
        super().__init__(config)

    def generate_server_code(self, interface: InterfaceDefinition) -> str:
        """Generate Python server code using Py4J GatewayServer."""
        methods = self._generate_python_methods(interface)
        imports = self._generate_python_imports(interface)

        host = self.config.host
        port = self.config.base_port
        name = interface.name
        name_lower = interface.name.lower()

        return textwrap.dedent(f"""\
            # Auto-generated by UPM Bridge Generator - Py4J Template
            # DO NOT EDIT MANUALLY

            {imports}

            import structlog

            try:
                from py4j.java_gateway import GatewayServer
            except ImportError:
                raise ImportError(
                    "py4j is required for Py4J bridge. Install with: pip install py4j"
                )

            logger = structlog.get_logger(__name__)


            class {name}EntryPoint:
                '''Py4J entry point for {name} interface.

                This class exposes Python methods to Java via Py4J.
                '''

                def __init__(self):
                    self._gateway = None

                def setGateway(self, gateway):
                    '''Set the Py4J gateway reference (called automatically).'''
                    self._gateway = gateway
                    return self

            {methods}

            def start_{name_lower}_bridge(
                host: str = "{host}",
                port: int = {port},
                auto_start: bool = True,
            ) -> GatewayServer:
                '''Start the Py4J gateway server for {name}.

                Args:
                    host: Host to bind to
                    port: Port to listen on
                    auto_start: Start the server immediately

                Returns:
                    GatewayServer instance
                '''
                entry_point = {name}EntryPoint()
                server = GatewayServer(
                    entry_point=entry_point,
                    address=host,
                    port=port,
                    auto_convert=True,
                )

                if auto_start:
                    server.start()
                    logger.info(
                        "Started {name} Py4J bridge",
                        host=host,
                        port=server.get_listening_port(),
                    )

                return server


            def main():
                '''Entry point for running the bridge server directly.'''
                server = start_{name_lower}_bridge()
                try:
                    server.server.serve_forever()
                except KeyboardInterrupt:
                    logger.info("Shutting down {name} bridge server")
                    server.shutdown()


            if __name__ == "__main__":
                main()
        """)

    def generate_client_code(self, interface: InterfaceDefinition) -> str:
        """Generate Java client code using Py4J."""
        methods = self._generate_java_client_methods(interface)
        imports = self._generate_java_imports(interface)

        namespace = self.config.namespace
        name = interface.name
        name_lower = interface.name.lower()

        # Build the Java class without using f-string for the body
        java_body = f"""// Auto-generated by UPM Bridge Generator - Py4J Template
// DO NOT EDIT MANUALLY

package {namespace}.{name_lower};

{imports}

/**
 * Py4J client for accessing {name} Python bridge.
 *
 * <p>This class provides a type-safe Java interface to Python
 * functionality exposed via Py4J.</p>
 */
public class {name}Client implements AutoCloseable {{

    private final Gateway gateway;
    private final {name}EntryPoint entryPoint;

    /**
     * Create a new {name} client.
     *
     * @param host Python bridge host
     * @param port Python bridge port
     */
    public {name}Client(String host, int port) {{
        this.gateway = new Gateway(host, port);
        this.entryPoint = new {name}EntryPoint(this.gateway);
    }}

    /**
     * Create a new {name} client with custom gateway settings.
     *
     * @param gateway Configured Py4J Gateway instance
     */
    public {name}Client(Gateway gateway) {{
        this.gateway = gateway;
        this.entryPoint = new {name}EntryPoint(this.gateway);
    }}

    /**
     * Get the underlying Py4J gateway.
     *
     * @return Gateway instance
     */
    public Gateway getGateway() {{
        return this.gateway;
    }}

    /**
     * Check if the connection to the Python bridge is alive.
     *
     * @return true if connected, false otherwise
     */
    public boolean isConnected() {{
        try {{
            return entryPoint != null && gateway != null;
        }} catch (Exception e) {{
            return false;
        }}
    }}

{methods}

    @Override
    public void close() {{
        if (gateway != null) {{
            gateway.shutdown();
        }}
    }}

    /**
     * Entry point wrapper for Py4J communication.
     */
    public static class {name}EntryPoint {{
        private final Gateway gateway;

        public {name}EntryPoint(Gateway gateway) {{
            this.gateway = gateway;
        }}

{methods}
    }}
}}
"""
        return java_body

